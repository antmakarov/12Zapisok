import UIKit

extension UIImage {
    
    class var successState: UIImage {
        return UIImage(named: "SuccessState")!
    }
    
    class var progressState: UIImage {
        return UIImage(named: "ProgressState")!
    }
    
    class var closeState: UIImage {
        return UIImage(named: "CloseState")!
    }
    
    class var appPlaceholder: UIImage {
        return UIImage(named: "appPlaceholder")!
    }
    
    class var cityPlaceholder: UIImage {
        return UIImage(named: "cityPlaceholder")!
    }
    
    class var notePlaceholder: UIImage {
        return UIImage(named: "notePlaceholder")!
    }
}
